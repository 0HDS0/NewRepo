    public class Class
    {
        //  Teste de software - Não consiste apenas em executar os testes. Executar testes é apenas uma das atividades.
        //Planejamento, análise, modelagem e implementação dos testes.
        //Testar o software não é somente verificar se os requisitos foram atendidos ou as especificações foram comtempladas
        //Testar não é igual a depurar.
        //A execução dos testes pode mostrar falhas causadas por defeitos no software.
        //É para garantir que o software funcione perfeitamente.

        //  Tipos de Testes:

        //  Unitários - são usados para pedaços do código, uma parte pequena da aplicação, sendo métodos e processos pequenos.

        //  Funcionais - pra validar as funcionalidades requisitos e reqras de negócios da aplicação.

        //  Operacionais - validam se a aplicação pode ser mantida em execução sem apresentar problemas, seja por muito ou pouco tempo.

        //  Positivo-Negativo - validam o fluxo de execução da aplicação, para assegurar que a aplicação
        //vai funcionar pelo "caminho feliz"/"mundo perfeito" de sua execução.

        //  Integração - Validam a integração entre os diferentes componentes da aplicação, uma vez que já tenham
        //sido testados em unidade, com o propósito de verificar se eles interagem corretamente por meio de suas interfaces.
        
        //  Regressão - validam o sistema sempre que algo for alterado, com o objetivo
        //de verificar se não surgiram novos erros em componetes já analisados.

        //  Caixa-Preta - Validam se as entradas de dados resultam nas saídas de dados pretendidas.
        //Ignora a estrutura interna do código, detalhes de implementação ou cenários de execução internos no software,
        //focando apenas nos requisitos funcionais do sistema.
        
        //  Caixa-Branca - Validam o código da aplicação, e asssim a criticidadea complexidade,
        //a estrutura eo nível do código implementado.

        //  Interface - Validam a navegabilidade e os elementos de tela estão funcionando corretamente, bem como se atende melhor a usabilidade do usuário.
        
        // Performance - Validam se o tempo de resposta da aplicação está dentro do desejado.

        //  Carga - São utilizados para ver se a plicação vai permanecer em funcionamento como esperado, mesmo havendo uma  quantidade de usuários simultâneos.

        //  Volume - valida a quantidade de dados envolvidos.

        //  Estresse - Validam a aplicação em situações inesperadas. Testar caminhos, que ás vezes não foram
        //previstos no decorrer do desenvolvimento ou da documentação.

        //  Configuração - Validam se a aplicação se mantem em funcionamento em diferentes ambientes de hardware.

        //  Compatibilidade - validam se a aplicação vai se manter em funcionamento, mesmo em diferentes ambientes de softwares e sistemas operacionais.

        //  Instalação/Desinstalação - Valida se a aplicação será instalada, desinstalada ou atualizada de forma corrreta.
        
        //  Segurança - Valida a segurança da aplicação.

        //  Processos são divididos em cinco:

        //  Planejamento - Estipula os tipos de teste que serão usados na aplicação

        //  Preparação - será asegurado que o ambiente para o teste é o ideal, para que tudo seja executado como planejado.
        //Envolve a preparação de equipe e equipamentos, porexemplo.

        //  Especificação - é o momento de elaboração e revisão dos casos de teste e dos roteiros de teste.

        //  Execução - é a fase em que as ações são executadas e há registro dos resultados.
         
        //  Entrega - Consiste na finalização do processo e arquivamento da documentação obtida

    }
}
